    // Methods to apply pixel colors and
    public BufferedImage getImage(int[][] data) {
        BufferedImage image = new BufferedImage(IMGSIZEX, IMGSIZEY,
                BufferedImage.TYPE_INT_RGB);
        for (int x = 0; x < IMGSIZEX; x++) {
            for (int y = 0; y < IMGSIZEY; y++) {
                int pixelColor = data[x][y];
                // Set Colors based on Binary Image value
                switch (pixelColor) {
                    case 0 -> pixelColor = Color.WHITE.getRGB();
                    case 1 -> pixelColor = Color.BLACK.getRGB();
                    case 2 -> pixelColor = Color.RED.getRGB();
                    case 3 -> pixelColor = Color.ORANGE.getRGB();
                    case 4 -> pixelColor = Color.BLUE.getRGB();
                    case 5 -> pixelColor = Color.CYAN.getRGB();
                    case 6 -> pixelColor = Color.GREEN.getRGB();
                    case 7 -> pixelColor = Color.DARK_GRAY.getRGB();
                    case 8 -> pixelColor = Color.GRAY.getRGB();
                    default -> {
                    }
                }
                image.setRGB(x, y, pixelColor);
            } // End for y.
        } // End for x.
        return image;
    } // End getData method.

    

